/**
 * Преобразует переданную строку в верхний регистр.
 * 
 * @param {string} str - Строка, которую необходимо преобразовать в верхний регистр.
 * @returns {string} Строка в верхнем регистре.
 */

function makeUpperCase(str) {
  return str.toUpperCase();
}


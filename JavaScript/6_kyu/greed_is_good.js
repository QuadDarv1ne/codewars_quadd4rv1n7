/*
[ Greed is Good ]

Описание игры:
"Greed" — это игра с пятью шестигранными кубиками.
Ваша задача — подсчитать очки за один бросок, следуя указанным правилам.

Правила начисления очков:
Три 1 → 1000 очков
Три 6 → 600 очков
Три 5 → 500 очков
Три 4 → 400 очков
Три 3 → 300 очков
Три 2 → 200 очков
Один 1 → 100 очков
Один 5 → 50 очков

Условия:
Один кубик может быть использован только один раз при расчете очков.
Например, если выпала тройка 5 (500 очков), то дополнительный 5 не может быть использован для получения еще 50 очков в этом же броске.

Примеры:
Для броска 5 1 3 4 1:
50 (за один 5)
100 (за два 1)
Общий результат: 250 очков

Для броска 1 1 1 3 1:
1000 (за три 1)
100 (за один 1)
Общий результат: 1100 очков

Для броска 2 4 4 5 4:
400 (за три 4)
50 (за 5)

Общий результат: 450 очков

Требования:
Решение не должно изменять исходный массив кубиков.
*/


function score(dice) {
    let score = 0;
    // Подсчитываем очки за тройки
    for (let i = 1; i <= 6; i++) {
        let count = dice.filter(die => die === i).length;
        if (count >= 3) {
            // Если есть три одинаковых, начисляем очки за них
            if (i === 1) {
                score += 1000; // Три 1 = 1000 очков
            } else {
                score += i * 100; // Три одинаковых других чисел
            }
            count -= 3; // Убираем тройку
        }
        // Подсчитываем очки за оставшиеся одиночные 1 и 5
        if (i === 1) {
            score += count * 100; // Одиночные 1 дают 100 очков
        } else if (i === 5) {
            score += count * 50;  // Одиночные 5 дают 50 очков
        }
    }
    return score;
}



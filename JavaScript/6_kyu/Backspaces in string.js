/*
[ Backspaces in string ]
[ Обработать строку с символами backspace ]

Предположим, что символ "#" действует как клавиша backspace в строке.
Это означает, что строка "a#bc#d" на самом деле равна "bd" — символ "#" удаляет предыдущий символ.

Ваша задача — обработать строку с символами "#" и вернуть строку, в которой все символы после обработки будут соответствовать этому правилу.

Примеры:
```
"abc#d##c" => "ac"
"abc##d######" => ""
"#######" => ""
"" => ""
```

Описание:
- Символ "#" удаляет предыдущий символ в строке.
- Если "#" в начале строки или подряд идет много символов "#", это означает, что они будут просто удалять предыдущие символы, пока не останется ничего.
- Если строка пуста, нужно вернуть пустую строку.
*/


function cleanString(s) {
    let result = [];
    for (let char of s) {
        if (char === '#') {
            result.pop(); // Удаляем последний элемент в массиве
        } else {
            result.push(char); // Добавляем символ в массив
        }
    }
    return result.join(''); // Преобразуем массив обратно в строку
}



/*
[ Help the bookseller ]
[ Помогите книгопродавцу ]

У книгопродавца есть много книг, классифицированных по 26 категориям, помеченных буквами от A до Z.
Каждая книга имеет код, состоящий как минимум из 3 символов.
Первый символ кода — это заглавная буква, которая определяет категорию книги.

В списке книг каждого кода следуют пробел и положительное число, которое указывает количество книг с данным кодом в наличии.

Задача: Вам нужно принять список книг продавца и список категорий. 
Ваша задача — найти общее количество книг в наличии для каждой категории из списка категорий.

Примечания:
- Коды книг в списке следуют в том же порядке, что и в списке категорий.
- Если один из входных списков пуст, необходимо вернуть пустую строку.
- Коды книг состоят из заглавных букв, и каждая книга имеет количество от 1 до n.
- Возвращаемый результат должен быть строкой, как в примере ниже.

Пример:
```
// Список книг:
"ABART 20", "CDXEF 50", "BKWRK 25", "BTSQZ 89", "DRTYM 60"

// Список категорий:
"A", "B", "C", "W"

// Результат:
"(A : 20) - (B : 114) - (C : 50) - (W : 0)"
```

Пояснение:
- Категория A: 20 книг (код ABART).
- Категория B: 25 книг (код BKWRK) + 89 книг (код BTSQZ) = 114 книг.
- Категория C: 50 книг (код CDXEF).
- Категория W: нет книг, поэтому 0 книг.
*/


function stockList(listOfArt, listOfCat) {
    // Если один из списков пустой, возвращаем пустую строку
    if (listOfArt.length === 0 || listOfCat.length === 0) {
        return '';
    }

    // Создаем объект для хранения суммарных книг по категориям
    let result = listOfCat.map(category => {
        let totalBooks = 0;
        
        // Ищем все книги, начинающиеся с данной категории
        listOfArt.forEach(book => {
            if (book.charAt(0) === category) {
                // Добавляем количество книг в эту категорию
                totalBooks += parseInt(book.split(' ')[1], 10);
            }
        });
        
        // Возвращаем строку для категории
        return `(${category} : ${totalBooks})`;
    });

    // Возвращаем результат как строку с категориями, разделёнными " - "
    return result.join(' - ');
}



/**
 * Функция для подсчета количества способов разменять заданную сумму денег с использованием заданных монет.
 * Порядок монет не имеет значения, а монет в распоряжении неограниченное количество.
 *
 * @param {number} money - Сумма, для которой нужно найти количество способов размена.
 * @param {number[]} coins - Массив монет (номиналов), которые можно использовать для размена.
 * @returns {number} Количество различных способов разменять заданную сумму.
 *
 * Пример:
 * countChange(4, [1, 2]) // => 3
 * countChange(10, [5, 2, 3]) // => 4
 * countChange(11, [5, 7]) // => 0
 */
function countChange(money, coins) {
  // Инициализация массива dp, где dp[i] - количество способов разменять сумму i
  const dp = new Array(money + 1).fill(0);
  
  // Есть один способ разменять сумму 0 — не использовать монеты
  dp[0] = 1;

  // Для каждой монеты в массиве coin
  for (let coin of coins) {
    // Обновляем dp для всех сумм от coin до money
    for (let i = coin; i <= money; i++) {
      dp[i] += dp[i - coin];
    }
  }

  // Ответ - количество способов разменять сумму money
  return dp[money];
}

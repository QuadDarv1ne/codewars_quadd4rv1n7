/**
 * Функция для нахождения максимальной абсолютной разницы в длинах строк между двумя массивами.
 * 
 * Функция принимает два массива строк и возвращает максимальную абсолютную разницу между длинами строк
 * из двух массивов. Если хотя бы один из массивов пуст, функция возвращает -1.
 *
 * Алгоритм:
 * - Если один из массивов пуст, возвращается -1.
 * - Для каждого массива вычисляются максимальная и минимальная длина строк.
 * - Вычисляется максимальная абсолютная разница между максимальной длиной одного массива
 *   и минимальной длиной другого массива.
 *
 * @param {Array<string>} a1 - Массив строк.
 * @param {Array<string>} a2 - Массив строк.
 * @returns {number} - Максимальная абсолютная разница между длинами строк в двух массивах,
 *                     или -1, если один из массивов пуст.
 */
function mxdiflg(a1, a2) {
  // Проверяем, если один из массивов пуст, возвращаем -1
  if (a1.length === 0 || a2.length === 0) {
    return -1;
  }

  // Находим максимальную и минимальную длину строк в каждом массиве
  let maxA1 = Math.max(...a1.map(str => str.length));
  let minA1 = Math.min(...a1.map(str => str.length));
  let maxA2 = Math.max(...a2.map(str => str.length));
  let minA2 = Math.min(...a2.map(str => str.length));

  // Возвращаем максимальную абсолютную разницу
  return Math.max(Math.abs(maxA1 - minA2), Math.abs(maxA2 - minA1));
}
